            <h3 class="mx-3 fs-4 text-decoration-underline">Document Verification Instructions</h3>
            <p class="mx-3  fs-6 mb-2 text-danger">
              During the document verification process, three options are available. The Study Centre must review the scanned documents submitted by the learner and select the appropriate status. The meaning of each status is explained below:
            </p>
            <ul>
              <li><strong>Verified</strong> – All submitted documents have been checked and found correct.</li>
              <li><strong>Re-Verified</strong> – One or more documents need correction. Please inform the learner to make the necessary changes.</li>
              <li><strong>Suspicious</strong> – The submitted document appears to be fake or doubtful and requires further review. Do not inform the learner. Such cases will be handled by the IT Cell, KKHSOU.</li>
            </ul>


